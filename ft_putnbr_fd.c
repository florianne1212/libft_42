#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int i;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		i = (((unsigned int)n) * -1);
	}
	else
		i = (unsigned int)n;
	if (i >= 0 && i <= 9)
	{
		ft_putchar_fd('0' + i % 10, fd);
	}
	else
	{
		ft_putnbr_fd(i / 10, fd);
		ft_putnbr_fd(i % 10, fd);
	}
}
